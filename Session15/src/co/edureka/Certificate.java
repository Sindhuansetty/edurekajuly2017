package co.edureka;

public class Certificate {

	int id;
	String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Certificate [id=" + id + ", name=" + name + "]";
	}
	
	
	
}
